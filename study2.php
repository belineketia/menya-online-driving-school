nzira nyabagendwa itari akayira ntibe n'inzira y'igitaka agomba kureka ibinyabiziga
bigenda muri iyo nzira nyabagendwa bigatambuka.
5. Umuyobozi wese uvuye ahantu hahana imbibi n'inzira nyabagendwa agiye
kwinjiramo agomba kureka ibinyabiziga bigendamo bigatambuka.
