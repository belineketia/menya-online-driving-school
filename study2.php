<h3>TWATEGETSE KANDI DUTEGETSE:</h3>
<h3>IGICE CYA MBERE: AMATEGEKO ABANZA</h3>
<h3>UMUTWE WA MBERE: IBIREBWA N'IRI TEKA</h3>

<h4>Ingingo ya 1:</h4>
Iri teka rigenga uburyo bwo kugenda mu nzira nyabagendwa, bwerekeye
abanyamaguru, ibinyabiziga, inyamaswa zikurura, izikorera ibintu, cyangwa izo
bagenderaho kimwe n'amatungo.
<h3>UMUTWE WA II: IBISOBANURO</h3>
<h4>Ingingo ya 2:</h4>
Mu gukurikiza ibivugwa muri iri teka:<br>
1. Ijambo "Inzira nyabagendwa" rivuga imbago zose z'imihanda minini,<br>
amabarabara, aho abantu nyamwinshi bahurira, aho imodoka zihagarara, inzira
n'utuyira two ku muhanda, ibiraro, ibyombo, mbese ku buryo rusange, imihanda
nyabagendwa yose igendwamo ku butaka;<br>
2. Ijambo "umuhanda", rivuga igice cyangwa ibice by'inzira nyabagendwa
bigendwamo n'ibinyabiziga, iyo nzira nyabagendwa ikaba yagira imihanda myinshi
itandukanyijwe ku buryo bugaragara n'ubutaka bwayo cyangwa ubusumbane;<br>
3. Ijambo "inzira y'ibinyabiziga", rivuga umuhanda n'inzira ziwukikije;<br>
4. Ijambo "igisate cy'umuhanda" rivuga kimwe mu bice bigabanyije umuhanda mu
burebure bwawo bishobora kugaragazwa n'umurongo umwe ukomeje cyangwa ugizwe
n'uduce dukulikiranye ariko icyo gisate kikagira ubugari buhagije ku buryo ibinyabiziga
bitali amagare, velometeri cyangwa amapikipiki bigenda bibangikanye;
2<br>
5. Ijambo "agahanda k'amagare" bivuga igice cy'inzira nyabagendwa cyagenewe
kunyurwamo n'amagare na velomoteri bikagaragazwa n'ikimenyetso cyabigenewe.<br>
6. Amagambo "isangano" ni "inkomane" bivuga ahantu hose imihanda ihurira, aho
umuhanda urasukira mu wundi cyangwa mu maharakubiri y'inzira nyabagendwa,
ubwo hakabarirwamo imyanya igizwe n'ayo mahuliro n'izo nkomane cyangwa n'ayo
maharakubiri y'imihanda.<br>
7. Ijambo "akayira" bivuga inzira nyabagendwa ifunganye yagenewe gusa
abanyamaguru cyangwa ibinyabiziga bigendera ku biziga bibiri.<br>
8. Ijambo "inzira y'igitaka" bivuga inzira nyabagendwa yagutse kurusha akayira
ariko ubusanzwe ikaba itaragenewe ibinyabiziga bibiri
Inzira y'igitaka ikomeza kwitwa ityo iyo isa n'umuhanda igihe irasutse gusa mu yindi
nzira nyabagendwa<br>
9. Ijambo "urusisiro" bivuga ahantu hose hari amazu yegeranye cyangwa afatanye,
ari ku ruhande urwo arirwo rwose rw'inzira nyabagendwa cyangwa se aho binjirira
n'aho basohokera hagaragazwa n'ibyapa by'aho hantu;<br>
10. Ijambo "umuyobozi" bivuga umuntu wese utwaye ikinyabiziga cyangwa uyobora
mu nzira nyabagendwa inyamaswa zikurura, zikorera cyangwa zigenderwaho, cyangwa
amatungo, yaba ubushyo cyangwa imwe imwe;
11. Ijambo "umukozi ubifitiye ububasha" bivuga umwe mu bakozi bavugwa mu
ngingo ya 3 y'iri teka, wambaye ku buryo bugaragaza ibimenyetso by'imirimo ashinzwe.
Iyo umugenzi ahagaze, umukozi ubifitiye ububasha ashobora kuba adafite ibyo
bimenyetso, ariko rero agomba kugaragaza ko abifitiye ububasha<br>
12. Ijambo "ikinyabiziga" bivuga ikintu cyose gikoreshwa mu gutwara abantu
n'ibintu ku butaka, kimwe n'igikoresho cyose kigendeshwa, gihingishwa, gikoreshwa
mu nganda cyangwa ahandi.<br>
13. Ijambo "ikinyamitende" bivuga ikinyabiziga cyose, nk'igare ry'ikiziga kimwe,
ry'ibiziga bibiri, bitatu cyangwa bine, kigendeshwa n'ingufu z'abantu bakiriho nko kuba
bakoresha ibirenge cyangwa intoki.<br>
14. a) Ijambo "igare" bivuga ikinyamitende cy'ibiziga bibiri;
b) Ijambo "velomoteri" bivuga ikinyabiziga gifite ibiziga bitarenze bibiri kandi gifite
moteri itarengeje santimetero kibe 50 cyangwa imbaraga zitarenga KvA 4 kandi,
hakurikijwe uko cyakozwe, umuvuduko wacyo nturenge Km 60 mu isaha.
3
Igare na velomoteri bidahetse ababigenderaho ntibifatwa nk'ibinyabiziga.<br>
15. Ijambo "ipikipiki" bivuga ikinyabiziga cyose cy'ibiziga bibiri gifite moteri,
ukuyemo za velomoteri;<br>
16. Amagambo "ikinyamitende itatu n'ikinyamitende ine bifite moteri" bivuga
ibinyabiziga by'ibiziga bitatu cyangwa bine bitarengeje uburemere bw’ibiro 400, kandi
moteri ikaba itarengeje santimetero kibe 350 cyangwa imbaraga zayo zikaba zitarengeje
KvA 15.
Gushyira intebe ku ruhande cyangwa romoruki inyuma y'ibinyabiziga byavuzwe ku
bika 13, 14, 15 na 16 ntacyo bihindura ku rwego birimo.<br>
17. Ijambo "ikinyabiziga kigendeshwa na moteri" bivuga ikinyabiziga cyose gifite
moteri ikigendesha kandi kigendeshwa n'ibikigize;<br>
18. Ijambo "imodoka" bivuga ikinyabiziga cyose kigendeshwa na moteri, uretse za
velomoteri, ipikipiki, ibinyamitende itatu cyangwa ine bifite moteri, n'imashini zikurura
n'izindi zihinga zifite moteri zikaba kandi ku busanzwe zitwara mu nzira nyabagendwa
abantu cyangwa ibintu cyangwa se zigenewe gukurura ibindi binyabiziga bitwara
abantu cyangwa ibintu;<br>
19. Ijambo "romoruki" bivuga ikinyabiziga kigenewe gukururwa n'ikindi; iryo jambo
rikoreshwa kandi kuri za makuzungu;<br>
20. Ijambo "makuzungu" bivuga romoruki iyo ariyo yose yagenewe gufatishwa ku
kinyabiziga gikurura ku buryo igice cyayo kiba kikiryamyeho kandi igice cy'uburemere
bwacyo ndetse n'ubw'ibyo itwaye bukaba bushikamiye icyo kinyabiziga;<br>
21. Ijambo "romoruki ntoya" bivuga romoruki iyo ariyo yose ifite uburemere
butarenga ibiro 750.<br>
22. Amagambo "ibinyabiziga bikomatanye" na "ibinyabiziga bikururana" bivuga
ibinyabiziga bifatanye bikagenda nk'aho ari kimwe;<br>
23. Ijambo "ikinyabiziga gifatanije" bivuga ikinyabiziga gikomatanye, kimwe ali
ikinyabiziga gikurura, ikindi ari makuzungu ;<br>
24. Ijambo "ikinyabiziga gikururana kabiri" bivuga ibinyabiziga bikomatanye
bikururana bigizwe n'ikinyabiziga gifatanije kandi kiriho romoruki yacyo;<br>
25. Amagambo "uburemere bwite" n'"uburemere bw'ikidapakiye", bivuga
uburemere bw'ikinyabiziga kizima gifite karosori n'ibikoresho byacyo ngombwa kandi
cyuzuye lisansi, mazutu cyangwa gazi, amazi n'amavuta, aliko hatabariwemo
abagitwaye, abantu cyangwa imizigo cyikoreye;<br>
26. Ijambo "uburemere bwikorewe" bivuga uburemere bwite bw'ikinyabiziga kizima
hongereweho uburemere bw'imizigo cyikoreye, ubw'ugitwaye n'ubw'undi muntu wese
gitwaye;<br>
4
27. Ijambo "uburemere ntarengwa bwemewe " bivuga uburemere bwose ntarengwa
bw'ikinyabiziga bwemejwe hakurikijwe ibivugwa muli iri teka, uburemere ntarengwa
bwemewe bw'ibinyabiziga bikomatanye, bw'ikinyabiziga gifatanije cyangwa
bw'ikinyabiziga gikurana kabiri, bwitwa "uburemere bugendanwa";<br>
28. Ijambo"guhagarara umwanya muto" bivuga igihe cya ngombwa ikinyabiziga
kimara gihagaze kugirango abantu cyangwa ibintu byinjire cyangwa bisohoke;<br>
29. Ijambo "guhagarara umwanya munini" bivuga igihe kirenze icya ngombwa
ikinyabiziga kimara gihagaze kugira ngo abantu cyangwa ibintu byinjire cyangwa
bisohoke;<br>
30. Ijambo "akagarura-rumuri" bivuga akantu karabagirana gasubiza imirasire
y'urumuri ku kintu kiyohereje;<br>
31. Ijambo "ikinyabiziga - ndakumirwa" ni ibinyabiziga by'abapolisi, ibizimyankongi ndetse n'ibinyabiziga bitwara abarwayi, iyo bijya aho bigomba gutabara
byihutirwa kandi bikarangwa n'intabaza irabagirana cyangwa irangurura ijwi;
32. Ijambo “Amatara y'urugendo" (amatara maremare) bivuga amatara y'ikinyabiziga
amurika umuhanda mu ntambwe ndende imbere y'icyo kinyabiziga;<br>
33. Ijambo” Amatara yo kubisikana" (amatara magufi) bivuga amatara y'ikinyabiziga
amurika inzira nyabagendwa imbere y'icyo kinyabiziga kitagombye guhuma cyangwa
kubangamira abayobozi bava mu cyerekezo.<br>
34. Ijambo “amatara ndangambere” bivuga amatara yikinyabiziga akiranga kandi
agaragaza ubugari bwacyo burebewe imbere.<br>
35. Ijambo "amatara ndanganyuma" bivuga amatara y'ikinyabiziga akiranga kandi
yerekana ubugari bwacyo burebewe inyuma;<br>
36. Ijambo "amatara kamena-bihu y'imbere" bivuga amatara y'ikinyabiziga
abonesha neza inzira imbere yacyo igihe cy'igihu, cy'imvura nyinshi, cy'urubura
cyangwa cy'umukungugu.<br>
37. Ijambo "amatara kamena-bihu y'inyuma" bivuga amatara y'ikinyabiziga akiranga
kandi yerekana ubugari bwacyo kirebewe inyuma igihe cy'igihu, cy'imvura nyinshi,
cy'urubura cyangwa cy'umukungugu mwinshi;<br>
38. Ijambo "amatara yo gusubira inyuma" bivuga amatara y'ikinyabiziga agenewe
kubonesha inzira inyuma y'ikinyabiziga no kumenyesha abandi bagenzi ko ikinyabiziga
gisubira inyuma cyangwa kigiye gusubira inyuma;<br>
39. Ijambo "itara ndanga-cyerekezo” cyangwa ikinyoteri" bivuga itara ry'ikinyabiziga
rigenewe kwereka abandi bagenzi ko umuyobozi ashaka kugana iburyo cyangwa
ibumoso;<br>
5
40. Amagambo "itara rishakisha" n'"itara rihindukizwa" bivuga, itara ry'ikinyabiziga
gishobora guhindukizwa n'umuyobozi bidatewe n'uko ikinyabiziga kigenda kandi
rishobora kumurika ikintu kiri mu nzira nyabagendwa cyangwa hirya yayo;<br>
41. Amagambo "itara ndangamubyimba" n'"itara ndanga burumbarare" bivuga itara
ry'ikinyabiziga ryerekana ubugari bwacyo, ahagana imbere cyangwa inyuma igihe
uburebure bwacyo burenga metero 6 cyangwa iyo ubugari bwacyo, habariwemo ibyo
cyikoreye, burenga metero 2,10<br>
42. Ijambo "ubuso bubonesha" bivuga ku byerekeye amatara, ubuso busohokana
urumuri, naho ku byerekeye utugarura rumuri bikavuga ubuso burabagiranamo
urumuri.<br>
43. Ijambo "itara ry'umuhondo" bivuga itara ryose ry'umuhondo wiganje,
ry'umuhondo nyawo cyangwa usa n'icunga hakurikijwe ibisobanuro bivugwa mu
ngereka ya 7 y'iri teka.<br>
<h2>UMUTWE WA III: ABAKOZI BABIFITIYE UBUBASHA</h2>
<h4> Ingingo ya 3 :</h4>
Bitabujije ububasha abagenza-cyaha bahawe n'amategeko y'umwihariko, abashinzwe
cyane cyane kubahiriza iri teka ni:
1. Ba ofisiye na ba suzofisiye bo muri Polisi y'igihugu igihe bari mu kazi;
2. Ba kaporari n’abapolisi bo muri Polisi y'Igihugu igihe bari mu kazi, babitumwe na
Prokireri wa Repubulika wo muri iyo fasi;
3. Abapolisi bo mu mutwe ushinzwe umutekano mu muhanda;
4. Abakozi bo mu biro by'amateme n'imihanda, ku byerekeye ibivugwa mu ngingo
ya 8 n'iya 9, iya 40, 60 kugeza kuya 68, iya 72 n'iya 73 iya 113 n'iya 114, iya 117 kugeza
kuya 120 z'iri teka;
5. Abakozi ba gasutamo, ku byerekeye ibivugwa mu ngingo ya 132;
6. Abakozi bo mu biro by'imisoro, ku byerekeye ibivugwa mu ngingo ya 121 kugeza
ku ya 135.
7. Abakozi b'ikigo cy'ubukerarugendo na pariki z'igihugu babiherewe uburenganzira
na Minisitiri w'Ubutabera;
8. Ba ofisiye bashinzwe Polisi y’igihugu igihe bayobora abasilikare bagenda ku
mirongo;
9. Abakozi bo mu biro bya Minisiteri ishinzwe Gutwara Abantu n’Ibintu mu mirimo
yayo babiherewe uburenganzira na Minisitiri w'Ubutabera
Abakozi bavugwa muri iyi ngingo bafite ububasha bwo kugenza ibyaha byica iri teka
batarengereye imilimo bashinzwe n'ifasi bakoramo.
6
UMUTWE WA IV: IMPANUKA.
Ingingo ya 4 :
Bitabangamiye ibiteganywa n’amategeko ahana, umugenzi wese uguweho n'impanuka
agomba:
1°. Guhita ahagarara igihe bimushobokeye kandi atagombye kubangamira bundi
bushya uburyo bwo kugenda mu muhanda cyangwa kwiteza ibyago ubwe;
2°. Gukora uko ashoboye kwose kugirango uburyo bwo kugenda mu muhanda bw'aho
impanuka yabereye bwoye guhungabana ashyira ibimenyetso ahashobora kubyara
inkomyi, nko gukoresha ikimenyetso n°A,29, kwakiriza icyarimwe amatara yose
ndangacyerekezo y'ikinyabiziga, cyangwa kuhatereka itara rimyatsa, ry'umuhondo
cyangwa risa n'icunga;
3°. Iyo umuntu yapfiriye mu mpanuka cyangwa yakomerekeyemo bikabije kandi bikaba
bidahungabanya uburyo bwo kugenda mu muhanda, kwirinda guhindura uko ibintu
bimeze, gusibanganya ibimenyetso bishobora kugaragaza abari mu makosa, no
kubimenyesha cyangwa gutumaho abubahiriza amahoro bari hafi aho;
4°. Iyo abandi Bantu bahuye n'iyo mpanuka babimusabye, kubabwira umwirondoro we;
5°. Iyo umuntu yapfiriye muri iyo mpanuka cyangwa yakomerekeyemo bikabije, ariko
akabona nta byago bishobora kumutera, kuguma aho impanuka yabereye cyangwa
kuhagaruka kugez'igihe abubahiriza amahoro bahagereye, cyeretse iyo bamwemereye
kuhava cyangwa iyo agomba gutabara abakomeretse cyangwa kujya kwivuza ubwe
bwite;
6°. Iyo impanuka yangije ibintu gusa cyangwa hakaba hari abakomeretse byoroheje,
kugana aho impanuka yabereye, kugirango, abyumvikanyeho n'uwo bagonganye,
bagaragaze uko byagenze, cyangwa niba batabyumvikanyeho, afashe umukozi
ubishinzwe kubyirebera.
Ariko rero, iyo umukozi ubifitiye ububasha adashobora cyangwa atashoboye kugera aho
byabereye mu gihe kiringaniye , abahuye n'impanuka bashobora buri wese
kubimureba, igihe bishoboka, kubimenyesha umutegetsi ushinzwe kubahiriza amahoro
uri hafi cyangwa uri aho batuye.
Nk'uko iyi ngingo ibiteganya:
- "umuntu wakomeretse cyane" bivuga umuntu wagize ibikomere bishobora
kumuviramo urupfu, kumushegesha wese, kumutesha igice cy'umubiri cyangwa
wacitse urugingo.
- "igikomere cyoroheje" bivuga igikomere cyose umuntu asanga kitahungabanya
ubuzima, kitamushegesha wese cyangwa kitatuma atakaza igice cy'umubiri cyangwa
urugingo.
7
UMUTWE WA V: IBITEGEKWA - IBYAKWA-IBIMENYETSO
Ingingo ya 5 :
1. Umugenzi wese uri mu nzira nyabagendwa agomba guhita yumvira ibitegetswe
n'abakozi babifitiye ububasha.
2. Ibitegekwa ni nk' ibi:
a) ukuboko kuzamuye, gutegeka abagenzi bose guhagarara keretse abageze mu
isangano bagomba guhita bahava;
b) ukuboko cyangwa amaboko atambitse ategeka guhagarara abaturuka mu
byerekezo bisanganya icyerekezo cyerekanwa n'ukuboko cyangwa amaboko arambuye.
c) kuzunguza intambike itara ritukura, bitegeka guhagarara abo iryo tara riganishaho.
3. Umugenzi wese ategetswe kugaragaza umwirondoro we igihe umukozi ubifitiye
ububasha abimusabye bitewe n' ikosa akoze ryica iri teka, cyangwa habaye impanuka.
4. Umuyobozi w' ikinyabiziga gihagaze umwanya muto cyangwa munini ategetswe
kugikuraho igihe abisabwe n' umukozi ubifitiye ububasha.
5. Umugenzi wese agomba kubahiriza ibimenyetso byashyiriweho gutunganya uburyo
bwo kugenda mu muhanda igihe cyose ibyo bimenyetso biteye uko byagenwe kandi
bigaragara bihagije.
6. Ibitegekwa n' abakozi babifitiye ububasha birusha agaciro ibindi bimenyetso.
UMUTWE WA VI: URUHUSHYA RWO GUTWARA IBINYABIZIGA.
ICYICIRO CYA MBERE: Uruhushya rwo gutwara ibinyabiziga
rutangiwe mu Rwanda.
Ingingo ya 6 :
1. Ntawe ushobora gutwara ikinyabiziga kigendeshwa na moteri mu nzira
nyabagendwa adafite kandi atitwaje uruhushya rwo gutwara ibinyabiziga rwatanzwe na
Polisi y’igihugu.
Uko uruhushya rwo gutwara ikinyabiziga ruteye n’uko rutangwa, bigenwa n’Iteka rya
Minisitiri ushinzwe Gutwara Abantu n’Ibintu abisabwe na Komité y’Igihugu ishinzwe
umutekano mu mihanda;
Umuyobozi agomba kwerekana ako kanya uruhushya rwo gutwara ibinyabiziga
arubajijwe n'umukozi ubifitiye ububasha.
2. Uruhushya rwo gutwara ibinyabiziga rwerekana urwego cyangwa inzego
z'ibinyabiziga cyatangiwe.
